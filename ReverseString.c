void reverseString(char *str) {
	int i, len = strlen(str);
	char temp;
	for(i=0; i<len/2; i++) {
		temp = str[i];
		str[i] = str[len-i-1];
		str[len-i-1] = temp;
	}
}


void _reverseString() {
	char str[] = "venkata";
	reverseString(str);
	printf("%s", str);
}


